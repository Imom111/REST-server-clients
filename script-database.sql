CREATE TABLE States(
	idState INT PRIMARY KEY AUTO_INCREMENT,
    name VARCHAR(255) NOT NULL
);

CREATE TABLE Municipalities(
	idMunicipality INT PRIMARY KEY AUTO_INCREMENT,
    name VARCHAR(255) NOT NULL,
    idStates_Municipalities INT NOT NULL,
    FOREIGN KEY (idStates_Municipalities) REFERENCES States(idState)
);

CREATE TABLE Customers(
	idCustomers INT PRIMARY KEY AUTO_INCREMENT,
    full_name VARCHAR(255) NOT NULL,
    phone VARCHAR(255) NOT NULL,
    email VARCHAR(255) NOT NULL,
    housing VARCHAR(255) NOT NULL,
    street VARCHAR(255) NOT NULL,
    postal_code INT NOT NULL,
    status BOOLEAN,
    idMunicipality_Customers INT NOT NULL,
    FOREIGN KEY (idMunicipality_Customers) REFERENCES Municipalities(idMunicipality)
);

